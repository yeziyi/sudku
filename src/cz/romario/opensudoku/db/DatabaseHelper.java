	private void insertFolder(SQLiteDatabase db, long folderID, String folderName) {
		long now = System.currentTimeMillis();
		db.execSQL("INSERT INTO " + SudokuDatabase.FOLDER_TABLE_NAME + " VALUES (" + folderID + ", " + now + ", '" + folderName + "');");
	}

